function initializeTestRun(testRun) {
    var diagnosticsComputer = createHost("DIAG COMP", 5, -3, 9, 9);

    var auxCore0 = createHost("AUX CORE 0", 16, 3, 3, 3);
    var auxCore1 = createHost("AUX CORE 1", 16, -2, 3, 3);
    createLink(diagnosticsComputer, 200, auxCore0, -1);
    createLink(diagnosticsComputer, 201, auxCore1, -1);

    // Add data files
    createNormalFile(diagnosticsComputer, 100, FILE_ICON_DATA, sampleData);
    createNormalFile(diagnosticsComputer, 101, FILE_ICON_DATA, sampleData);
    var realFile = createNormalFile(diagnosticsComputer, 200, FILE_ICON_DATA, inputData);
    setFileInitiallyCollapsed(realFile);
    var realFile2 = createNormalFile(diagnosticsComputer, 201, FILE_ICON_DATA, inputData);
    setFileInitiallyCollapsed(realFile2);

    // Define success condition
    createTable("REPORT", 108, 0, "Calculate the life support rating of the submarine");
    addTableOutput("OUTPUT", [2555739], createRegister(diagnosticsComputer, 9, 5, "OUTP"));
}

